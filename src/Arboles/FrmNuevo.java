    public void estado() {
        if (!simulador.estaVacio()) {
            txtEstado.setText("Árbol vacío");
        } else {
            txtEstado.setText("Árbol con elementos");
        }
    }

    public void limpiar() {
        txtApellido.setText(null);
        txtNombre.setText(null);
        txtEdad.setText(null);
        txtBuscar.setText(null);
        txtcedula.setText(null);
        txtdato.setText(null);
        jubilado();
    }

    public void jubilado() {
        modelo.setLista(lista);
        tblJubilados.setModel(modelo);
        tblJubilados.updateUI();
    }

