    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        try {
            if (Utilitario.isEmpty(txtcedula.getText()) || Utilitario.isEmpty(txtApellido.getText()) || Utilitario.isEmpty(txtNombre.getText())
                    || Utilitario.isEmpty(txtEdad.getText()) || Utilitario.isEmpty(txtdato.getText())) {
                JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
                if (!u.validadorDeCedula(txtcedula.getText())) {
                    JOptionPane.showMessageDialog(this, "Error, verifique cedula");
                    txtcedula.setText(null);
                }
            } else {
                int dato = Integer.parseInt(txtdato.getText());
                String nom = txtNombre.getText();
                String app = txtApellido.getText();
                String ced = null;
                if (Utilitario.validadorDeCedula(txtcedula.getText())) {
                    ced = txtcedula.getText();
                } else {
                    //JOptionPane.showMessageDialog(this, "Cedula Incorrecta");
                    txtcedula.setText(null);
                }

                int edad = Integer.parseInt(txtEdad.getText());
                this.inicializar(true);
                complementos();
                if (this.simulador.insertar(dato, nom, app, ced, edad)) {
                    JOptionPane.showMessageDialog(null, "El dato fue insertado correctamente", " ...", 1);
                    limpiar();
                    if (dato >= 29 || edad >= 59) {
                        lista.add(new Empleado(dato, nom, app, ced, edad));
                    }
                    this.inicializar(true);

                    complementos();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se pudo insertar el dato", "Intenta de nuevo...", 0);

        }
    }//GEN-LAST:event_BtnRegistrarActionPerformed

